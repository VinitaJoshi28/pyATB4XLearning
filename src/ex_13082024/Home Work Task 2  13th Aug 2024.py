# max
# pow num1 to num2
# sub, mul, sum, div.
# Format your out with f{""}
num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))
print("The maximum number result is=  "f"{max(num1,num2)}")
print("The subtraction result is =  "f"{num1-num2}")
print("The power result is = "f"{pow(num1,num2)}")
print("The multiplication result is = "f"{num1*num2}")
